package javaBasics;

public class Variables_DataTypes {

	public static void main(String[] args) {
		short my_variable = 499;
		float my_decimal = (float)4.5; //typecasting the double value to the float value 
		double my_double = 11.5;
		char my_char = 	'A';
		boolean is_true = false;
		
		System.out.println(my_variable);
		System.out.println(my_decimal);
		System.out.println(my_double);
		System.out.println(my_char);
		System.out.println(is_true);
	}
	/*	PRIMITIVE DATA TYPES
	 * byte (number, 1byte)
	 * short (number, 2bytes)
	 * int (number, 4bytes)
	 * float (float number, 4bytes)
	 * double (float number, 8bytes)
	 * char (a character, 2bytes)
	 * boolean (true or false, 1byte)
	 * */

}
