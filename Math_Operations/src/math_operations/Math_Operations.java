package math_operations;

public class Math_Operations {

    public static void main(String[] args) {
        System.out.println(Math.abs(-23.44));//absolute value
        System.out.println(Math.ceil(14.30));//rounds off
        System.out.println(Math.floor(17.99));//rounds down
        System.out.println(Math.pow(4,2));
    }

}
