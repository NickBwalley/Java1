package math_operations;

public class Math_Operations {

    public static void main(String[] args) {
        System.out.println(Math.abs(-23.44));//absolute value
        System.out.println(Math.ceil(14.30));//rounds off
        System.out.println(Math.floor(17.99));//rounds down
        System.out.println(Math.pow(4,2));//firstArg is the Base number secondArg is Number Raised to??
        System.out.println(Math.sqrt(811));//gets SquareRoot of a Number
        System.out.println(Math.max(19.99998, 19.99999));//maximum of the TwoValues
        
    }

}
