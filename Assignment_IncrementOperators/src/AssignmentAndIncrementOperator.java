
public class AssignmentAndIncrementOperator {

	public static void main(String[] args) {
		int fish = 79;
		int lee = 99;
		
		lee = lee + 5;
		System.out.println(lee);
		lee += 9;
		System.out.println(lee);
		lee *= 9;
		System.out.println(lee);
		System.out.println(++fish);
		System.out.println(fish++);
		System.out.println(fish);
	}

}
